package com.dwlarson.joshua.jgfx;

public class JGFXException extends Exception {
	public JGFXException() {
		super();
	}
	
	public JGFXException(String message) {
		super(message);
	}
	
	public JGFXException(String message, Throwable cause) {
		super(message, cause);
	}
	
	public JGFXException(Throwable cause) {
		super(cause);
	}
}
